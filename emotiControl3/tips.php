                <p>La tristeza es una emoción natural que todos experimentamos. Permítete sentir tristeza y busca apoyo en amigos, familiares o un profesional si es necesario. Practicar técnicas de auto-cuidado, como la meditación o la escritura, también puede ayudar a procesar y superar la tristeza.</p>
